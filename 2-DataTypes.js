// There are three types of variables in JS 
// let, var, const 
// -- const -- 
// Value is locked, we cannot change the value after it is assigned on time.

// -- let -- 
// let is only {} scope we use it now.

// -- var -- 
// works on the block level scope, prefer not to use var 

const name = "Ariha";
console.log(name);
var age = 20;
let grade = "A";
console.log(grade);
console.log(age);

let a;
console.log(a);  //if not initialized than it will be undefined.